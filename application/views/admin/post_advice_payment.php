	<h5>Можно использовать в тексте страницы с платежными реквизитами</h5>
	<ul>
		<li><code>%order_number%</code> - номер заказа cо значком №. Пример: <strong>№24</strong>.</li>
		<li><code>%order_number_raw%</code> - номер заказа, только цифры. Пример: <strong>24</strong>.</li>
		<li><code>%order_total%</code> - сумма заказа с доставкой в удобочитаемом виде. Пример: <strong>1 383.52 руб. (с учетом доставки 300 руб.)</strong>.</li>
		<li><code>%order_total_raw%</code> - сумма заказа, только цифры. Пример: <strong>383.52</strong>.</li>
		<li><code>%order_total_nodelivery%</code> - сумма заказа без доставки в удобочитаемом виде. Пример: <strong>1 083.52 руб.</strong>.</li>
		<li><code>%order_total_nodelivery_raw%</code> - сумма заказа без доставки, только цифры. Пример: <strong>1083.52</strong>.</li>
		<li><code>%order_delivery%</code> - стоимость доставки, в удобочитаемом виде. Пример: <strong>300 руб.</strong></li>
		<li><code>%order_delivery_raw%</code> - стоимость доставки, только цифры. Пример: <strong>300</strong>.</li>
		<li><code>%email%</code> - e-mail заказчика. Пример: <strong>someone@example.com</strong>.</li>
		<li><code>%customer_id%</code> - уникальный номер заказчика. Пример: <strong>256</strong>.</li>
		<li><code>%customer_name%</code> - имя заказчика. Пример: <strong>Егоров Олег</strong>.</li>
		<li><code>%customer_address%</code> - адрес заказчика. Пример: <strong>123000, г. Москва, ул. Тверская, 12-22</strong>.</li>
		<li><code>%success_url%</code> - URL страницы оповещения об успешном проведении платежа.</li>
		<li><code>%fail_url%</code> - URL страницы оповещения об ошибке проведении платежа.</li>
	</ul>
</div>